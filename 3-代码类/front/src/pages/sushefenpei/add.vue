<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            sushemingcheng : false,
            susheleixing : false,
            xibie : false,
            zhuanye : false,
            fenpeishijian : false,
            xuehao : false,
            xueshengxingming : false,
            xingbie : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          sushemingcheng: '',
          susheleixing: '',
          xibie: '',
          zhuanye: '',
          fenpeishijian: '',
          xuehao: '',
          xueshengxingming: '',
          xingbie: '',
        },
        sushemingchengOptions: [],
        xuehaoOptions: [],
        rules: {
          sushemingcheng: [
            { required: true, message: '宿舍名称不能为空', trigger: 'blur' },
          ],
          susheleixing: [
            { required: true, message: '宿舍类型不能为空', trigger: 'blur' },
          ],
          xibie: [
          ],
          zhuanye: [
          ],
          fenpeishijian: [
          ],
          xuehao: [
            { required: true, message: '学号不能为空', trigger: 'blur' },
          ],
          xueshengxingming: [
          ],
          xingbie: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.fenpeishijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='sushemingcheng'){
              this.ruleForm.sushemingcheng = obj[o];
              this.ro.sushemingcheng = true;
              continue;
            }
            if(o=='susheleixing'){
              this.ruleForm.susheleixing = obj[o];
              this.ro.susheleixing = true;
              continue;
            }
            if(o=='xibie'){
              this.ruleForm.xibie = obj[o];
              this.ro.xibie = true;
              continue;
            }
            if(o=='zhuanye'){
              this.ruleForm.zhuanye = obj[o];
              this.ro.zhuanye = true;
              continue;
            }
            if(o=='fenpeishijian'){
              this.ruleForm.fenpeishijian = obj[o];
              this.ro.fenpeishijian = true;
              continue;
            }
            if(o=='xuehao'){
              this.ruleForm.xuehao = obj[o];
              this.ro.xuehao = true;
              continue;
            }
            if(o=='xueshengxingming'){
              this.ruleForm.xueshengxingming = obj[o];
              this.ro.xueshengxingming = true;
              continue;
            }
            if(o=='xingbie'){
              this.ruleForm.xingbie = obj[o];
              this.ro.xingbie = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/sushexinxi/sushemingcheng', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.sushemingchengOptions = res.data.data;
          }
        });
        this.$http.get('option/xuesheng/xuehao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.xuehaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      sushemingchengChange () {
        this.$http.get('follow/sushexinxi/sushemingcheng?columnValue=' + this.ruleForm.sushemingcheng, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.susheleixing){
              this.ruleForm.susheleixing = res.data.data.susheleixing
            }
            if(res.data.data.xibie){
              this.ruleForm.xibie = res.data.data.xibie
            }
            if(res.data.data.zhuanye){
              this.ruleForm.zhuanye = res.data.data.zhuanye
            }
            if(res.data.data.xueshengxingming){
              this.ruleForm.xueshengxingming = res.data.data.xueshengxingming
            }
            if(res.data.data.xingbie){
              this.ruleForm.xingbie = res.data.data.xingbie
            }
          }
        });
      },
      // 下二随
      xuehaoChange () {
        this.$http.get('follow/xuesheng/xuehao?columnValue=' + this.ruleForm.xuehao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.susheleixing){
              this.ruleForm.susheleixing = res.data.data.susheleixing
            }
            if(res.data.data.xibie){
              this.ruleForm.xibie = res.data.data.xibie
            }
            if(res.data.data.zhuanye){
              this.ruleForm.zhuanye = res.data.data.zhuanye
            }
            if(res.data.data.xueshengxingming){
              this.ruleForm.xueshengxingming = res.data.data.xueshengxingming
            }
            if(res.data.data.xingbie){
              this.ruleForm.xingbie = res.data.data.xingbie
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('sushefenpei/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('sushefenpei/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('sushefenpei/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('sushefenpei/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

